require("config.lazy")
vim.cmd("set tabstop=4")          -- Number of spaces that a tab counts for
vim.cmd("set shiftwidth=4")       -- Number of spaces to use for each step of (auto)indent
vim.cmd("set expandtab")           -- Use spaces instead of tabs
vim.cmd("set smartindent")         -- Makes indenting smart
vim.cmd("set autoindent")          -- Copy indent from current line when starting a new line
vim.cmd("set softtabstop=4")       -- Number of spaces a <Tab> counts for while editing

